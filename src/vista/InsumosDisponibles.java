    private void btnfiltroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnfiltroActionPerformed
        modta2 = new DefaultTableModel();
        String sql = "";
        String select = comboBuscar.getSelectedItem().toString();

        try {
            if (select.equalsIgnoreCase("Codigo")) {

                int cod = Integer.parseInt(txtbuscar.getText());
                sql = " select * from inventarios where COD_INV=" +cod+" and CANT_EXISTENCIA>0";
            }
            if (select.equalsIgnoreCase("Nombre")) {
                String nom = txtbuscar.getText();
                sql = " select * from inventarios where NOMBRE like'" + nom + "%'"+" and CANT_EXISTENCIA>0 ";
            }
           
            ora2.consultar(sql);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, " Llenar los campos correctamente ");
        }

        try {
            int numCol = ora2.getRstmdata().getColumnCount();
            for (int i = 0; i < numCol; i++) {
                modta2.addColumn(ora2.getRstmdata().getColumnLabel(i + 1));

            }

            while (ora2.getRst().next()) {
                Object[] fila = new Object[numCol];
                for (int i = 0; i < numCol; i++) {
                    fila[i] = ora2.getRst().getObject(i + 1);
                }

                modta2.addRow(fila);
                tablaasingi.setModel(modta2);
            }

        } catch (Exception e) {
        } finally {

            if (modta2.getRowCount() == 0) {
                JOptionPane.showMessageDialog(null, " No se encontraron registros ");
                limpCampos();
            }

        }

    }//GEN-LAST:event_btnfiltroActionPerformed

    private void btncargardetainsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncargardetainsActionPerformed
        try {
            int codinv = Integer.parseInt(txtcodinv.getText());
            int cantexis = Integer.parseInt(txtcantexis.getText());
            int cantreq = Integer.parseInt(txtcantreq.getText());
            if (cantexis >= cantreq) {

                try {

                    CallableStatement cs = ora2.getCon().getConexion().prepareCall("call p_decrExiste(?,?)");
                    cs.setInt(1, codinv);
                    cs.setInt(2, cantreq);
                    cs.execute();
                    DetalleInsumo.txtcodinvent.setText(""+codinv);
                    DetalleInsumo.txtcantidad.setText(""+cantreq);
                    limpCampos();

                } catch (Exception e) {
                }

            } else {
                if (cantexis > 0 && cantexis<cantreq) {

                    int respuesta = JOptionPane.showConfirmDialog(null, " Solo quedan  " + cantexis + "unidades \n"
                        + " Desea tomarlas ", "Tomar unidades ", JOptionPane.YES_NO_CANCEL_OPTION);
                    switch (respuesta) {
                        case JOptionPane.YES_OPTION:

                        try {

                            CallableStatement cs = ora2.getCon().getConexion().prepareCall("call p_decrExiste(?,?)");
                            cs.setInt(1, codinv);
                            cs.setInt(2, cantexis);
                            cs.execute();
                            DetalleInsumo.txtcodinvent.setText(""+codinv);
                            DetalleInsumo.txtcantidad.setText("" + cantexis);
                             limpCampos();
                        } catch (Exception e) {
                        }

                        break;
                        case JOptionPane.NO_OPTION:
                          limpCampos();
                        break;
                        case JOptionPane.CANCEL_OPTION:
                         limpCampos();
                        break;
                    }

                } else {
                    JOptionPane.showMessageDialog(null, " Solicitar  pedido del insumo ");
                   
                }
            }

        } catch (Exception e) {

        }
    }//GEN-LAST:event_btncargardetainsActionPerformed

